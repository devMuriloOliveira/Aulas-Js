// Convenções de nomenclatura de variáveis em JavaScript
// Regras: podem começar com letra, $ ou _, mas NÃO podem começar com números ou símbolos especiais

// ERRADO: não pode começar com número
// let 3nome = "Murilo"

// CORRETO: número pode estar no final do nome
let nome3 = "Murilo"

// CORRETO: pode usar o símbolo $ no nome da variável
let nome$ = "Murilo"

// CORRETO: pode começar com underscore (_)
let _nome = "Murilo"

// ERRADO: não pode começar com símbolos especiais como @
// let @nome = "Murilo"

// ERRADO: não pode começar com símbolos especiais como !
// let !nome = "Murilo"
