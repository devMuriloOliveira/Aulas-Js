// Math é um objeto nativo do JavaScript que fornece funções matemáticas

// Math.max(): retorna o maior número entre os argumentos fornecidos
let maiorNumero = Math.max(10, 20, 30, 40, 50)
console.log(maiorNumero) // Exibe: 50

// Math.min(): retorna o menor número entre os argumentos fornecidos
let menorNumero = Math.min(10, 20, 30, 40, 50)
console.log(menorNumero) // Exibe: 10

// Math.random(): retorna um número aleatório entre 0 (inclusive) e 1 (exclusive)
// Ou seja, retorna um número decimal entre 0 e 0.999...
let numeroAleatorio = Math.random()
console.log(numeroAleatorio) // Exibe um número aleatório como: 0.123456789