// Variáveis são espaços na memória que armazenam valores
// A palavra-chave 'let' declara uma variável que pode ser reatribuída

// Declara uma variável chamada 'nome' e atribui o valor "Murilo"
let nome = "Murilo"

// Template literal: usa backticks (`) e ${} para interpolar variáveis na string
console.log(`O nome do usuário é ${nome}`) // Exibe: O nome do usuário é Murilo

// Declara uma variável chamada 'idade' e atribui o valor numérico 18
let idade = 18

// Operação aritmética: soma o valor da variável idade com ela mesma
console.log(idade + idade) // Exibe: 36 (18 + 18)

