// JavaScript possui valores especiais para números que representam casos extremos

// Infinity: representa um número infinito positivo (resultado de divisões por zero ou números muito grandes)
console.log(Infinity); // Exibe: Infinity

// -Infinity: representa um número infinito negativo
console.log(-Infinity); // Exibe: -Infinity

// NaN: significa "Not a Number" (Não é um Número)
// Ocorre quando uma operação matemática não pode produzir um número válido
console.log(NaN); // Exibe: NaN

// Todos esses valores especiais são do tipo "number" em JavaScript
console.log(typeof Infinity); // Retorna: "number"
console.log(typeof -Infinity); // Retorna: "number"
console.log(typeof NaN); // Retorna: "number" (mesmo não sendo um número válido!)
