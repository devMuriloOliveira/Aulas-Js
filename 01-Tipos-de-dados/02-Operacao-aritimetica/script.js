// Operações aritméticas em JavaScript sempre retornam números (tipo "number")
// O operador typeof verifica o tipo de dado do resultado da operação

// Adição: soma dois números
console.log(typeof (10 + 10)); // Retorna: "number" (resultado: 20)

// Subtração: subtrai o segundo número do primeiro
console.log(typeof (10 - 10)); // Retorna: "number" (resultado: 0)

// Multiplicação: multiplica dois números
console.log(typeof (10 * 10)); // Retorna: "number" (resultado: 100)

// Divisão: divide o primeiro número pelo segundo
console.log(typeof (10 / 10)); // Retorna: "number" (resultado: 1)

// Módulo: retorna o resto da divisão entre dois números
console.log(typeof (10 % 10)); // Retorna: "number" (resultado: 0, pois 10 dividido por 10 não tem resto)

// Exponenciação: eleva o primeiro número à potência do segundo (10 elevado a 10)
console.log(typeof (10 ** 10)); // Retorna: "number" (resultado: 10000000000)

