// Valores "vazios" ou especiais em JavaScript que representam ausência de valor ou casos especiais

// null: representa a ausência intencional de valor (é um valor atribuído propositalmente)
// É um valor primitivo que indica "nenhum valor" ou "vazio"
console.log(null); // Exibe: null

// undefined: representa uma variável que foi declarada mas não recebeu um valor
// Também é retornado quando uma propriedade não existe em um objeto
console.log(undefined); // Exibe: undefined

// NaN: "Not a Number" - resultado de operações matemáticas inválidas
// Exemplo: 0/0, Math.sqrt(-1), ou tentar converter uma string não numérica
console.log(NaN); // Exibe: NaN

// Infinity: representa infinito positivo (número maior que qualquer número finito)
// Pode ocorrer em divisões por zero ou operações com números muito grandes
console.log(Infinity); // Exibe: Infinity

// -Infinity: representa infinito negativo (número menor que qualquer número finito)
console.log(-Infinity); // Exibe: -Infinity
