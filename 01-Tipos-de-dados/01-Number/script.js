// O operador typeof retorna o tipo de dado de um valor
// Em JavaScript, números inteiros e decimais são do tipo "number"

// Verifica o tipo do número inteiro 123
console.log(typeof 123); // Retorna: "number"

// Verifica o tipo do número decimal 123.456
console.log(typeof 123.456); // Retorna: "number"

// Verifica o tipo do número negativo -123.456
console.log(typeof -123.456); // Retorna: "number"